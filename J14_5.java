public class J14_5 {

    public static void main(String[] args) {

        for (int i = 1; i<=10; i++) //rysuje tabliczkę mnożenia
        {
            for (int j = 1; j<=10; j++)
            {
                System.out.print(i*j);
                System.out.print(" ");
            }

            System.out.println();
        }

    }
}
